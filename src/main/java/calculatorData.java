public class calculatorData {
    double calculatorSum(double a, double b){
        return a+b;
    }
    double calculatorSubtruct(double a, double b){
        return a-b;
    }
    double calculatorMultiply(double a, double b){
        return a*b;
    }
    double calculatorDevide(double a, double b){
        return a/b;
    }
}
